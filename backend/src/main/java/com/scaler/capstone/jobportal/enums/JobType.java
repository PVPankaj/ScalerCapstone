package com.scaler.capstone.jobportal.enums;

/**
 * Enumeration representing the common engagement models for a job.
 */
public enum JobType {
    /** Standard full‑time employment. */
    FULL_TIME,

    /** Part‑time employment. */
    PART_TIME,

    /** Contract‑based work with a defined end date. */
    CONTRACT,

    /** Short‑term or seasonal temporary jobs. */
    TEMPORARY,

    /** Internship positions, typically for students or fresh graduates. */
    INTERNSHIP,

    /** Freelance or gig‑based jobs. */
    FREELANCE
}
